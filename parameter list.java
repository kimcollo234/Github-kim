public class Main {
    static void myMethod(String fname, int age) {
        System.out.println(fname + " is" + age);

    }

    public static void main(String[] args) {
        myMethod("Joyy", 19); // Joyy is 19 years
        myMethod("Kenny", 32);// Kenny is 32 years
        myMethod("Collins", 21);// Collins is 21 years
    }
}